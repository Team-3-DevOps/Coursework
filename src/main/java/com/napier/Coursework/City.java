package com.napier.Coursework;

/*
Represent an city
 */

public class City {
    /**
     * City ID
     */
    public int ID;

    /**
     * City Name
     */
    public String Name;

    /**
     * City's country code
     */
    public String CountryCode;

    /**
     * district of the city
     */
    public String District;

    /**
     * City population
     */
    public int Population;

}

