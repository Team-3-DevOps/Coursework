package com.napier.Coursework;

/*
Represent an city
 */

public class City {
    /**
     * City ID
     */
    protected int ID;

    /**
     * City Name
     */
    protected String Name;

    /**
     * City's country code
     */
    protected String CountryCode;

    /**
     * district of the city
     */
    protected String District;

    /**
     * City population
     */
    protected int Population;
    /**
     * City's continent
     */
    protected String Continent;

}

